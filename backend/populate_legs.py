sys.path.append(os.path.dirname(os.path.abspath(__file__)))

from database import SessionLocal, ExerciseCatalog

def populate_legs():
    db = SessionLocal()
    
    # Переименовываем старую категорию если есть
    db.query(ExerciseCatalog).filter(ExerciseCatalog.category == "НОГИ").update({"category": "НОГИ, ЯГОДИЦЫ, ИКРЫ"})
    
    legs_exercises = [
        "Приседания со штангой",
        "Приседания в Смите",
        "Приседания в Смите с узкой постановкой ног",
        "Приседания в Смите с акцентом на ягодицы",
        "Приседания в Гакке",
        "Обратные приседания в Гакке",
        "Фронтальный присед",
        "Приседания с гантелью на груди",
        "Приседания плие с гантелью",
        "Приседания с нижним блоком",
        "Выпады с гантелями/штангой",
        "Выпады в Смите с акцентом на ягодицы",
        "Выпады с гантелями с акцентом на ягодицы",
        "Болгарские выпады в Смите",
        "Болгарские выпады с гантелями",
        "Жим ногами",
        "Жим ногами с акцентом на ягодицы",
        "Жим ногами в тренажере сидя",
        "Разгибания ног в тренажере",
        "Сгибания ног лежа в тренажере на бицепс бедра",
        "Сгибания ног на бицепс бедра в тренажере стоя по 1-й ноге",
        "Сгибания ног с гантелью на бицепс бедра",
        "Мертвая тяга",
        "Румынская тяга",
        "Румынская тяга по 1-й ноге",
        "Ягодичный мостик",
        "Отведение ноги в кроссовере",
        "Сведения ног в тренажере",
        "Разведения ног в тренажере",
        "Экстензия с акцентом на ягодицы",
        "Тяга в Смите на ягодицы",
        "Тяга нижнего блока на ягодицы",
        "Подъемы на носки в тренажере сидя",
        "Подъемы на носки в тренажере стоя/в Смите",
        "Подъемы на носки по 1-й ноге с гантелью"
    ]
    
    new_cat = "НОГИ, ЯГОДИЦЫ, ИКРЫ"
    existing = db.query(ExerciseCatalog.name).filter(ExerciseCatalog.category == new_cat).all()
    existing_names = [e[0] for e in existing]
    
    added_count = 0
    for name in legs_exercises:
        if name not in existing_names:
            db.add(ExerciseCatalog(name=name, category=new_cat))
            added_count += 1
            
    db.commit()
    db.close()
    print(f"Добавлено {added_count} новых упражнений в категорию '{new_cat}'")

if __name__ == "__main__":
    populate_legs()
